from turtle import*
rad=120
setheading(-150)
penup()
color("red")
begin_fill()
forward(120)
pendown()
right(90)
circle(-rad,120)
forward(rad*3**.5)
left(120)
circle(2*rad,120)
left(60)
forward(rad*3**.5)
end_fill()

left(180)
color("green")
begin_fill()
forward(rad*3**0.5)
left(120)
circle(2*rad,120)
left(60)
forward(rad*3**.5)
left(180)
circle(-rad,120)
end_fill()

left(180)
penup()
circle(rad,120)
pendown()
color("yellow")
begin_fill()
circle(rad,120)
right(180)
forward(rad*3**.5)
right(60)
circle(-2*rad,120)
right(120)
forward(rad*3**.5)
end_fill()

penup()
left(98)
forward(6)
setheading(60)
pendown()
color("blue")
begin_fill()
circle(distance(0,0))
end_fill()
hideturtle()
penup()
while True:
    forward(10)
    left(180)
    forward(10)
    left(180)